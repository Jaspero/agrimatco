        <h1>Folijarna prihrana lucerne</h1>

        <p>Lucerna je jedna od navažnijih biljaka za hranidbu svih vrsta domaćih životinja. Osim što je bogata vitaminima, to je biljka sa visokim sadržajem bjelančevina, a najveći dio nalazi se u lišću.</p>
        <p>To je biljka koja ima dva stadija razvoja: prvi stadij se odigrava u prvoj godini u kojoj se lucerna uglavnom ukorjenjuje (Rootex), kada je prinos sijena mali te drugi stadij koji se odigrava u ostalim godinama, a prinos sijena je daleko veći negoli u prvoj godini.</p>
        <p><img src="strucni-savjeti/lucerna/i1.jpg" alt=""></p>

        <h2>Folijarna prihrana lucerne</h2>
        <p>U početnom porastu lucerne od 8-10 cm visine vrlo je bitan razvoj korijenovog sustava i visoki protok fosfora.  U ovoj fazi preporuča se korištenje rootexa (0,5 kg/ha na 200-300L vode/ha) i trazexa ili oligomixa (0,5-1 kg/ha na 200-300L vode/ha).</p>
        <ul>
            <li>Rootex je stimulator rasta i razvoja korijena, a trazex je visoka koncentacija mikroelemenata u fazama rasta i cvjetanja.</li>
        </ul>
        <p>Nakon prvog otkosa, 10 cm visine lucerne, koristiti bioplex u količini 0,6 L/ha na 200-300L vode/ha.</p>
        <p><img src="strucni-savjeti/lucerna/i2.jpg" alt=""></p>
        <p>Nakon drugog otkosa, 10 cm visine lucerne, koristiti bioplex u količini 0,6 L/ha na 200-300L vode/ha.</p>
        <p>Nakon svakog otkosa, 10 cm visine,   i porasta lucerne do faze 10-15 cm, obavezna je primjena Bioplexa 0,6 L/ha na 200-300L vode/ha. Bioplex pospješuje kondiciju usjeva, otpornost na sušu, bolje ukorijenjavanje i preživljavanje.</p>
