        <p>Također se preporuča primjena biostimulatora Bioplexa koji pospješuje otpornost biljke na niske temperature i pomaže biljkama da se oporave od stresa nakon presadjivanja. Primjenjuje se u intervalima 10-14 dana, pri zaštiti protiv bolesti i štetnika zajedno sa gnojivima.</p>
        <p>Koriste se u folijarnoj primjeni 0,3-0,5L/ha ili kroz sustav kap na kap 0,1-0,15 ml/1L vode.</p>

