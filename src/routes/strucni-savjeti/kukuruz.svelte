        <h1>Folijarna prihrana kukuruza</h1>

        <p>Kukuruz ima visok proizvodni potencijal pa da bi se taj potencijal iskoristio, gnojidbom se treba osigurati sva potrebna hranjiva u dovoljnoj količini.</p>
        <p> Prihrana je važna agrotehnička mjera u većini poljoprivrednih kultura, pa tako i u kukuruzu jer tada biljke usvajaju najviše hraniva unešenih u tlo ili putem lista, a koja poslije utječu na kvantitetu i kvalitetu prinosa.</p>
        <p><img src="strucni-savjeti/kukuruz/i.png" alt=""></p>

        <p>Kukuruz osnovna hraniva, dušik, fosfor i kalij, usvaja tijekom vegetativnog stadija do oprašivanja. Fosfor i kalij na većini tala unose se gnojidbom u osnovnoj obradi.</p>
        <p>Gnojidbom u osnovnoj obradi, hraniva fosfor i kalij se raspodjeljuju u zoni najviše razvijenog korijenovog sustava. U odnosu na fosfor i kalij, dušik je hranivo koje je u pravilu bolje primijeniti u dopunskoj obradi tla.</p>
        <p>Folijarnu prihranu potrebno je obavljati u ranim jutarnjim ili kasnim poslijepodnevnim satima kako bi se izbjeglo oštećenje lista.</p>
        <p>Gnojiva primijenjena folijarno biljka odmah usvaja, ali kukuruz podnosi male koncentracije i potrebno je koristiti posebno pripremljena gnojiva za tu svrhu.</p>
        <p>Folijarna prihrana kukuruza:</p>

        <figure>
            <figcaption><b>1. faza</b></figcaption>
            <ul>
                <li>Od početka razvoja listova pa do 6. Lista kukuruza, da bi se ubrzao porast i ostavrio brži razvoj korijena preporuča se korištenje sljedećeg:</li>
                <li>
                    <ul>
                        <li>Vodotopivo gnojivo Novalon 12-48-6+TE (3kg/ha) ili Amcopaste (2kg/ha) na 200-300 L vode/ha.</li>
                        <li>Trazex, gnojivo sa visokom koncentracijom mikroelemenata u fazama rasta, u količini 0,5-1 kg/ha na 200-300 L vode/ha.</li>
                    </ul>
                </li>
                <li>Novalon i Trazex se mogu miješati sa herbicidima, a za poboljšanje učinka folijarnih gnojiva preporuča se Inex, okvašivač i penetrator u količini 150 ml/ha.</li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>2. faza</b></figcaption>
            <ul>
                <li>Za kondiciju usjeva i poboljšanje otpornosti na stresne uvjete u fazi od 6. do 9. lista, preporuča se korištenje vodotopivog gnojiva Novalon 20-20-20+TE (3kg/ha) ili Amcopaste (3kg/ha) na 200-300 L vode/ha, kao i gnojiva sa visokom koncentracijom cinka, Kelatex Zn u količini 1 kg/ha na 200-300 L vode/ha. </li>
                <li>Novalon i Kelatex Zn se mogu miješati sa herbicidima na bazi sulfuron urea, nema zastoja u rastu,  a za poboljšanje učinka folijarnih gnojiva preporuča se Inex, okvašivač i penetrator u količini 150 ml/ha.</li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>3. faza</b></figcaption>
            <ul>
                <li>U fazi intenzivnog porasta (do 80cm) koristiti Novalon 20-20-20+TE (5-7 kg/ha na 200-300L vode) i Bioplex (0,3-0,5 L/ha na 200-300L vode).</li>
                <li>Novalon i Bioplex su važni za razvoj lisne mase i akumulaciju asimilata, povećavaju otpornost biljke na sušu, doprinose povećanju prinosa i kvaliteti zrna.</li>
                <li>Bioplex je biostimulator koji se treba koristiti tokom cijele vegetacije, posebno u nepovoljnim uvjetima, nakon primjene herbicida i prilikom svake gnojidbe. U Bioplexu je veliki utjecaj organskih ekstrakata, aminokiselina i fitohormona, smanjujenegativni učinak herbicida, bolesti i štetnika.</li>
                <li>Koristiti ga u dozi 0,5L/ha na 200-300L vode.</li>
            </ul>
        </figure>

