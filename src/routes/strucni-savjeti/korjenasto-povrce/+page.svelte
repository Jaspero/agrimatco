    <h1>Prihrana korjenastog povrća</h1>

    <p>
      Mrkva, peršin, celer, pastrnjak i cikla, spadaju u skupinu povrća kojemu za rast i razvoj
      pogoduju umjereno topla i vlažna područja. Sjeme klija pri relativno niskim temperaturama, već
      na 3 do 4 °C, a samo cikla na 6 do 8 °C. Mlade biljčice relativno su otporne na kasne
      proljetne mrazeve, što omogućuje sjetvu korjenastog povrća relativno rano u proljeće. Isto
      tako i rani jesenski mrazevi ne nanose im značajnije štete.
    </p>
