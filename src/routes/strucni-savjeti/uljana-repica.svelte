        <h2>Folijarna prihrana uljane repice:</h2>

        <figure>
            <figcaption><b>1. faza – faza rasta stabljke</b></figcaption>
            <ul>
                <li>U ovoj fazi može doći do blokade postojećeg fosfora u tlu zbog niskih temperatura i visoke vlage, te je veoma bitno biljci dati visoku koncentraciju fosfora kojom se pospješuje ubrzan porast, bolji razvoj korijena i stvaranje generativnih organa.</li>
                <li>Preporuča se korištenje sljedećih preparata:</li>
                <li>
                    <ul>
                        <li>Novalon 12-48-6+TE, vodotopivo gnojivo za prihranu biljke u ranoj fazi u vrijeme formiranja korijenovog  sustava te za intenzivnu cvatnju. Visoka koncentracija fosfora će pospješiti razvoj korijenovog sustava, utjecati će na bolju vegetaciju, a količina kalija če utjecati na bolju otpornost biljke na bolesti i nepovoljne uvjete. Koristiti ga u količini 3 kg/ha na 200-300L vode/ha sa mogućnosti miješanja sa fungicidima i insekticidima.</li>
                        <li>Kelatex B je gnojivo sa visokom koncentracijom bora koje treba primjeniti u količini 0,5-1 kg/ha na 200-300L vode/ha pošto poboljšava fiksaciju dušika, povećava zametanje i oplodnju, otpornost na bolesti, hladnoću i sušu.</li>
                        <li>Bioplex, biostimulator, koristiti u količini 0,5L/ha na 200-300L vode/ha.</li>
                        <li>Inex, okvašivač i penetrator, poboljšava učinak folijarnih gnojiva i treba ga koristiti u količini 150 ml/ha na 200-300L vode/ha.</li>
                    </ul>
                </li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>2. faza – faza kada je stabljika 50-70 cm, prije izbijanja cvjetnih pupova</b></figcaption>
            <ul>
                <li>U fazi velike potrebe sumpora u razvoju uljane repice preporuča se kombinacija Agrofol 650 ili Amcopotato + Bioplex + Inex. Ovom kombinacijom se pospješuje kondicija usjeva, produžena cvatnja i povećana uljnost i prinos.</li>
                <li>
                    <ul>
                        <li>Agrofol 650 je tekuće gnojivo s visokim sadržajem sumpora i koristi se u dozi 5-7L/ha na 200-300L vode/ha, dok je Amcopotato praškasta vodotopiva formulacija, koja uz dušik, sadrži i visoku koncentraciju fosfora, kalij, magnezij, cink i sumpor i koristi se u dozi 3 kg/ha na 200-300L vode/ha.</li>
                        <li>Bioplex, biostimulator, koristiti u količini 0,3-0,5L/ha na 200-300L vode/ha.</li>
                        <li>Inex, okvašivač i penetrator, poboljšava učinak folijarnih gnojiva i treba ga koristiti u količini 150 ml/ha na 200-300L vode/ha.</li>
                    </ul>
                </li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>3. faza – nakon cvatnje</b></figcaption>
            <ul>
                <li>Da bi se povećali uljnost, prinos, kvaliteta zrna preporuča se vodotopivo gnojivo Novalon 6-12-36+3MgO+TE u količini 4-6 kg/ha na 200-300L vode/ha.</li>
                <li>Preporuča se primjena Bioplexa, biostimulatora, u količini 0,3-0,5 L/ha na 200-300 L vode/ha zajedno sa insekticidima i fungicidima.</li>
                <li>Inex, okvašivač i penetrator, poboljšava učinak folijarnih gnojiva i treba ga koristiti u količini 150 ml/ha na 200-300L vode/ha.</li>
            </ul>
        </figure>

        <p>Tokom cijele vegetacije, u posebno nepovoljnim uvjetima, nakon primjene herbicida i prilikom svake gnojidbe, obavezna je primjena Bioplexa 0,5-0,7 L/ha na 200-300L vode/ha. Koristiti ga za smanjenje negativnog učinka herbicida, bolesti i štetnika jer u sebi ima veliki utjecaj organskih ekstrakta, aminokiselina i fitohormona.</p>

