        <h1>Folijarna gnojidba lijeske</h1>

        <p>Prinos lijeske ovisi o velikom radu koji pravovremeno treba obaviti. Kako je pravilno gnojiti i zaštititi od bolesti i štetnika. Lijeska je vrlo zahvalna višegodišnja kultura, ali još premalo zastupljena. Uspjeh u uzgoju i dobivenom prinosu lijeske ovisi o velikom radu koje treba pravovremeno i na odgovarajući način obaviti. Jedan od važnih radova u uzgoju lijeske je provođenje meliorativne gnojidbe koju treba obaviti nakon kemijske analize tla. Na taj način tlo se opskrbljuje osnovnim makroelementima (NPK). Od četvrte godine gnoji se kompletna površina mineralnim NPK gnojivima i to u jesenskom periodu koristeći gnojiva sa visokim udjelom kalija (oko 150 kg/ha kalij), a u proljeće je potrebno vršiti prihranu dušikom (od 250-300 kg/ha KAN).</p>
        <p>Osim nužne zaštite protiv bolesti i štetnika te gnojidbe granuliranim gnojivima za potpuni uspjeh u uzgoju lijeske, potrebno je, u određenim fazama rasta, obavezno koristiti određene formulacije folijarnih gnojiva i biostimulatore. Kompletni biostimulator - BIOPLEX - je odličan za jačanje biljnih funkcija u periodima veće metaboličke aktivnosti lijeske.</p>

        <figure>
            <figcaption>BIOPLEX je organski proizvod koji povećava otpornost na niske i ektremno visoke temperature, a ima brojne korisne karakteristike:</figcaption>
            <ul>
                <li>Povećava otpornost na bolesti i štetnike;</li>
                <li>Primjenjuje se u bilo kojoj fazi rasta u dozi 0,5 - 1 l/ha;</li>
                <li>Sadrži fitohormone auksin, giberelin i citokinin, L-aminokiseline i organske ekstrate.</li>
            </ul>
        </figure>

        <p>Primjena Bioplexa izvodi se s folijarnim gnojivima i pesticidima. Folijarna gnojidba je dopunska prihrana koja će uveliko utjecati na prinos i kvalitet plodova.</p>

        <figure>
            <figcaption>Zašto je bitna folijarna gnojidba lijeske?</figcaption>
            <ul>
                <li>Nadomješta nedostatak hranjiva kada je to biljci najpotrebnije;</li>
                <li>1 kg hranjiva kojeg biljka prima preko lista zamjenjuje višestruko veću količinu od one koju prima preko korijena, npr: N 1:4, Mg 1:75, Zn 1:12, Fe 1:100, B 1:5;</li>
                <li>Niske temperature u proljeće, višak vlage u tlu, sušni period, oštećenja uslijed tuče i drugi nepovoljni uvjeti neće uvelike utjecati na prinos ako se koriste određena folijarna gnojiva i biostimulator.</li>
            </ul>
        </figure>

        <figure>
            <figcaption>Na tržištu su preporuka NOVALONI - folijarna gnojiva različitih formulacija za pojedine razvojne faze lijeske koja osim osnovnih makroelemenata (NPK) sadrže i bitne mikroelemente (B, Cu, Fe, Mn, Mo, Zn).</figcaption>
            <ul>
                <li>Ne sadrže otrovne i netopive spojeve ;</li>
                <li>Lako se tope i ne rade grudice te nema štopanja dizni ;</li>
                <li>Primjena je kroz sustav kap po kap i folijarno preko lista.</li>
            </ul>
        </figure>

        <p>Novalon dolazi u raznim formulacijama: Novalon 12:48:6 + TE, Novalon 19:6:20 + 2 MgO + TE, Novalon 20:20:20 + TE, Novalon 15:5:35 + TE, Novalon 6:12:36 + 3MgO + TE, Keymag 10:0:40 + 2MgO, Novalon 0:5:35 + 4,5MgO + TE.-</p>
        <p>Od velike važnosti je i pravovremena primjena bitnih sekundarnih (Ca) i mikroelemenata (B, Cu, Fe, Mn, Mo, Zn) koji imaju veliku ulogu u periodu formiranja pupova, cvjetanju i oplodnji te je obavezna primjena kad se formiraju plodovi i počinje razvoj jezgre u ljusci. Upravo Trazex, Kelatex B, Tropicel, Kelatex Zn - koji sadrže bitne mikroelemenate i L-aminokiseline i organske ekstrate dokazano utječu na rast i razvoj pune jezgre u ljusci i samog zdravlja lijeske.</p>
        <p>Za puni učinak djelovanja pesticida i folijarnih gnojiva potrebno je koristiti okvašivač, aktivator i penetrator INEX koji omogućava puni učinak pesticida te ne dozvoljava da se otopina u atomizerima pjeni te skida postojeću pjenu sa pesticida koji se pjene (na 500 l vode ide 0,25 l INEX).</p>

