        <h1>Folijarna prihrana vinove loze</h1>

        <p>S obzirom na to da se svake godine iz tla odnose značajne količine dušika, fosfora, kalija, kalcija i ostalih hranjiva, potrebno ih je gnojidbom nadoknaditi. Unošenjem hranjiva preko tla ili preko lista (folijarno), osiguravamo vinovoj lozi brz rast i razvoj, održavamo ravnotežu, opseg i kvalitetu uroda, te općenito utječemo na zdravstveno stanje vinove loze.</p>
        <p>Količina hranjivih tvari koje se godišnje iznesu iz tla u vinogradu, ovisi o urodu, načinu obrade, vrsti tla i nagibu terena. Da bismo to utvrdili potrebno je obaviti  fizikalnu i kemijsku analizu tla.</p>
        <p><img src="strucni-savjeti/vinova-loza/i6.jpg" alt=""></p>

        <h2>Prednosti prihrane putem lista</h2>
        <p>Analizom listova vinove loze utvrđeno je da sadržaj hranjivih tvari nije statičan te da se mijenja prema fazama vegetacije vinove loze. Najveću količinu hranjivih tvari vinova loza sadrži na samom početku vegetacije, a tijekom vegetacije njihova se količina postupno smanjuje.</p>
        <p>Folijarna gnojidba je primjena direktno preko lista. Hranjiva usvojena putem lista kroz biljku se prenose provodnim tkivom do ostalih organa.</p>
        <p>Prednost folijarne gnojidbe u odnosu na gnojidbu putem tla je u bržem usvajanju i iskorištavanju ciljanih hranjiva (mikroelementi Fe, B, Zn....) te uklanjanju uočenih nedostataka u mnogo kraćem vremenu. Uvjet za efikasnu primjenu gnojiva putem lista je dobro razvijena lisna masa. Folijarna gnojidba je najekonomičnija i najbrža za primjenu. Hranidba putem lista je u odnosu na hranidbu putem tla, 3-4 puta brža.</p>
        <p>Prilikom primjene fungicida i insekticida preporuča se primjena folijarnih gnojiva. Ishrana vinove loze uz primjenu folijarne ishrane, rezultira puno boljim rezultatima.</p>

        <h2>Folijarna prihrana vinove loze</h2>

        <figure>
            <figcaption><b>1. faza – porast mladica vinove loze 20-30 cm</b></figcaption>
            <ul>
                <li>U fazi porasta mladica vinove loze 20-30 cm za bolji rast mladica preporuča se vodotopivo gnojivo Novalon 20-20-20+TE (3 kg na 400-600L vode/ha) + Bioplex (0,5L na 400-600L vode/ha).</li>
                <li>Novalon 20-20-20+TE i Bioplex su kompatibilni s većinom sredstava za zaštitu bilja.</li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>2. faza – desetak dana prije cvatnje</b></figcaption>
            <ul>
                <li>U fazi prije cvatnje koristiti Tropicel ili Kelatex B u količini 1-1,5 kg/ha na 400-600L vode/ha.</li>
                <li>Bor ima esencijalnu važnost za prijenos asimiliranih tvari unutar biljke, daje otpornost na hladnoću i sušu, otpornost na patogene, te povećava plodnost peludipa time i pospješuje oplodnju (ujednačena oplodnja).</li>
                <li>Kelatex B ili Tropicel (bakreni preparati) su kompatibilni s većinom sredstava za zaštitu bilja, osim sa visoko alkalnim sredstvima.</li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>3. faza – pred cvjetanje-intenzivan porast</b></figcaption>
            <ul>
                <li>U fazi pred cvjetanje-intenzivan porast koristiti gnojivo Kalex Cu (1-1,5L/ha na 400-600L vode/ha) + Trazex (0,5 kg/ha na 400-600L vode/ha).</li>
                <li>Korištenjem ovih preparata pospješuje se oplodnja i razvoj generativnih organa.</li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>4. faza – nakon cvjetanja vinove loze</b></figcaption>
            <ul>
                <li>Nakon cvjetanja vinove loze koristiti vodotopivo gnojivo Novalon 20-20-20+TE u količini 3 kg na 400-600L vode/ha + Bioplex(0,6L na 400-600L vode/ha) ili Trazex (0,5 kg na 400-600L vode/ha) i po potrebi Kelatex Fe (visoka koncentracija željeza).</li>
                <li>Bioplex poboljšava otpornost biljke na bolesti, dok Trazex u sebi sadrži visoku koncentraciju mikroelemenata i organskih ekstrakata.</li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>5. faza – formiranje bobica</b></figcaption>
            <ul>
                <li><img src="strucni-savjeti/vinova-loza/i2.jpg" alt=""></li>
                <li>Gnojivo Kelatex B (visoka koncentracija bora) ili Tropicel (gnojivo za bolju cvatnju i oplodnju) poboljšavaju kvalitetu bobica jer u nedostatku bora odumiru vrhovi mladica i boboice ostaju sitne.</li>
                <li><img src="strucni-savjeti/vinova-loza/i4.jpg" alt=""></li>
                <li>Koristiti 1 kg na 400-600 L vode.</li>
                <li><img src="strucni-savjeti/vinova-loza/i3.jpg" alt=""></li>
                <li>Novalon 6-12-36+3MgO+TE poboljšava kvalitetu, vitalnost i zdravlje loze. Visoka koncentracija kalija i magnezija (usljed velikog nedostatka Mg, primjeniti Mainstay Mg u količini 2L/ha) u ovoj fazi je vrlo važna te se ova formulacija gnojiva primjenjuje u količini 3 kg na 400-600L vode.</li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>6. faza – zatvaranje grozda (pojava šaranja)</b></figcaption>
            <ul>
                <li>U ovoj fazi koristiti Novalon 0-5-35+4,5MgO+TE u dozi 3-4 kg na 400-600L vode ili Agro-K (1 kg/ha) + Bioplex 0,6L na 400-600 L vode.</li>
                <li>Bioplex se može primjenjivati tokom cijele vegetacije, u posebno nepovoljnim uvjetima i sprečava pojavu bortritisa – bobice ne pucaju.</li>
            </ul>
        </figure>

        <figure>
            <figcaption><b>7. faza – 7-10 dana nakon zatvaranja grozda</b></figcaption>
            <ul>
                <li>U ovoj fazi koristiti Novalon 0-5-35+4,5MgO+TE u dozi 3-4 kg na 400-600L vode ili Barrier (1L/ha na 400-600L vode), čime se poboljšava odnos šećera i kiselina, te utječe na veći prinos i kvalitet.</li>
                <li>Važno je u ovoj fazi ne primjenjivati dušik.</li>
