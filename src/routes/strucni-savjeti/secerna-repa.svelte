        <h1>Folijarna prihrana šećerne repe</h1>
        <a href="katalozi/2020-prihrana.pdf#page=50">Pročitaj</a>

        <p>Šećerna repa je industrijska biljka koja se uzgaja za proizvodnju šećera, zbog visoke koncentracije saharoze u njezinom zadebljanom korijenu. Šećerna repa sadrži 75 % vode, 16 - 18 % šećera, 5 - 6 % celuloze i 2 - 3 % ostalih supstanci, uključujući i minerale.</p>
        <p>Za proizvodnju visokih prinosa dobre kvalitete korijena šećerne repe tlo, pored ostalog, treba imati dobre zalihe bora i magnezija, jer ih šećerna repa iznosi više nego druge kulture, i to bora  300-500 g/ha te magnezija 60-80 kg/ha.</p>
        <p>Pri nedostatku bora proizvodnju visokih prinosa dobre kvalitete korijena šećerne repe tlo, pored ostalog, treba imati dobre zalihe bora, jer pri nedostatku bora nastaju morfološke anatomske i fiziološke promjene na biljkama šećerne repe. Takve biljke ostaju nerazvijene, izumiru začeci najmlađih listova, a na glavi i  unutrašnjoj strani peteljke nastaju pukotine. U uvjetima nedostatka bora korijen repe ostaje nerazvijen, a sadržaj je šećera u njemu smanjen. Nedostatak magnezija u tlu također uzrokuje smanjenje prinosa i kvalitete korijena. Taj element, osim što je prijeko potreban za promet energije i rezervnih tvari, ulazi u sastav molekule klorofila. Zato se prvi simptomi nedostatka magnezija pokazuju u obliku ograničenih žutih područja na rubovima listova srednje dobi. Žuta područja šire se između žila i kroz nekoliko tjedana lišće postaje nekrotično.</p>
        <p>Fosfor ima vrlo veliku ulogu u izgradnji korijena (Rootex), koji je posebno bitan u  početku vegetacije.</p>
        <p>Nedostatak fosfora uzrokuje sporiji i slabiji razvoj biljke (manji prinos), te sporije i slabije nakupljanje šećera (manji sadržaj šećera)</p>

