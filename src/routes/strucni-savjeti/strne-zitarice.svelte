        <p>Folijarna prihrana strnih žitarica osigurava visoke prinose, pospješuje usjeve i izvrstan je nadomjestak prilikom nedostatka hranjiva.</p>
        <p>U modernoj poljoprivrednoj proizvodnji visoki prinos pšenice predstavlja imperativ uspješne proizvodnje. Tri su glavna kriterija za visoki prinos pšenice: broj klasja po hektaru, broj zrna u klasu i težina zrna. Smanjenje prinosa pšenice može uzrokovati kvaliteta tla, nedostatak hranjiva i vremenske neprilike poput suše ili tuče.</p>
        <p>Kako bi se postigao maksimalan učinak, potrebna je optimalna kombinacija preparata za folijarnu prihranu: Novalon, Trazex, Bioplex i Inex.</p>
        <p>Folijarna prihrana se izvodi dva puta: prvi put pred kraj faze busanja i početkom faze vlatanja, dok se druga folijarna prihrana izvodi pred kraj faze vlatanja i početkom faze klasanja.</p>
        <p>Novalon kao vodotopivo gnojivo uz dušik sadrži fosfor i kalij kako bi osigurao uravnotežen spektar helatiziranih mikroelemenata. Njegova primjena osigurava optimalno usvajanje hranjiva neovisno o pH vrijednosti tla ili vode.</p>
        <p>Trazex je snažna formula koja osim visoke koncentracije mikroelemenata poput cinka, željeza, bakra, bora i mangana sadrži i L-aminokiseline i organske ekstrakte koji pomažu boljem usvajanju hranjiva, a rezultat primjene je poticanje vegetativnog rasta, cvatnje, rano formiranje ploda, povećanje metaboličke aktivnosti biljke, te brža i efikasnija asimilacija mikrohranjiva.</p>
        <p>Bioplex je kompleksni biostimulator koji je izvstan za jačanje biljnih funkcija. Sadrži makro i mikro elemente kao i različite supstance prirodnog podrijetla koji stimuliraju fiziološke procese u biljci.</p>
        <p>Inex kao okvašivač, aktivator i penetrator smanjuje površinsku napetost vode i povećava kontaktnu površinu lista. Djeluje kao pojačivač primjenjenih folijarnih gnojiva i otporan je na ispiranje, te ne stvara pjenu u prskalicama.</p>
        <p>Primjenom sva tri preparata prinosi će biti optimizirani na najisplativiji način.</p>
