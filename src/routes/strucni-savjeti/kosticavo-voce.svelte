        <h1>Folijarna prihrana koštičavog voća</h1>
        <a class="prihrana-link" href="katalozi/2023-gnojidba-prihrana-zastita.pdf#page=53">Pročitaj</a>

        <p>Prema obliku ploda voćne vrste - marelica, breskva, višnja, trešnja, šljiva, nektarina, spadaju u grupu koštićavog voća. Za svoj rast i razvoj traže duboka, strukturirana tla, bogata hranjivima i organskom tvari. Intenzivan uzgoj koštićavog voća obuhvaća gnojidbu na zalihu, odnosno meliorativnu gnojidbu, a kasnije gnojidbu prema životnim i godišnjim ciklusima.</p>
        <p>Listopad je mjesec u kojem se uglavnom većina voćnih vrsta nalazi u fazi priprema za prelazak u zimsko mirovanje.</p>
        <p>Koštičavo voće trešnja,višnja ,kajsija,šljiva,breskva,../ za start u proljeće priprema se u jesen zadnjom folijarnom prihranom i to prije opadanja lišča dok je još lišče fiziološki aktivno. Folijarnom prihranom se stvara rezerva u korjenu i kori drveta /korjenove dlačice i korijen rastu u jesenskom periodu i akumuliraju hranjiva .</p>
        <p>Hranjivi elementi dušik, cink, bor mogu biti limitirajuči faktori u proizvodnji koštičavog voča, a to je posebno izraženo na tlima visokog pH ( preko 7 ) te ako tla sadrže veću količinu fosfora ili se koristi nedozreli stajnjak (postoji blokada hraniva).</p>
        <p>Potrebe koštičavih voćaka za dušikom, borom, cinkom posebno su izražene u proljeće kada kreće vegetacija te će ih biijka koristiti iz rezerve stvorene jesenskom prihranom preko lista.</p>

