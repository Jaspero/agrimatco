    <h1>Folijarna prihrana jezgričavog voća</h1>

    <p>Prema obliku ploda voćne vrste jabuka, kruška, dunja, mušmula, spadaju u jezgričavo voće.</p>
    <p>
      Zbog posebnosti korijenovog sustava voćaka kao višegodišnjih kultura, njihova ishrana je
      znatno složenija nego ishrana jednogodišnjih kultura.
    </p>
    <p>
      Gnojidbom se regulira trajanje vegetacije, otpornost na mrazove, bolesti i štetnike, zatim
      kvaliteta i trajnost plodova kao i dugovječnost voćaka.
    </p>
    <p>
      U redovitoj gnojidbi razlikuje se meliorativna gnojidba, gnojidba mladih nasada te gnojidba
      nasada u punoj rodnosti. Utvrđivanje količine pojedinih biljnih hraniva određuje se prema
      rezultatima kemijske analize tla.
    </p>
    <p>
      S proizvodnog stajališta najvažnije je razdoblje pune rodnosti voćaka. Redovita gnojidba
      dugogodišnjih nasada obavlja se dva ili više puta godišnje, a sastoji se od osnovne gnojidbe i
      folijarne prihrane.
    </p>

