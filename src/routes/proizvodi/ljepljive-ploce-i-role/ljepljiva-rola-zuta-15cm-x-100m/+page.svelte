    <h1>Ljepljiva rola za insekte Žuta - 15 cm × 100 m</h1>
    <p>
      FLORA ljepljiva plastična traka za insekte je umotana u rolu sa dvostranim ljepljivim slojem
      koji ima snažan učinak hvatanja. Jednostavne su za korištenje, nisu toksične i predstavljaju
      praktično rješenje za suzbijanje štetočina. Koriste se u plastenicima, vrtovima, plantažama,
      hortikulturi i farmama jer privlače i hvataju insekte poput: Bjele mušice I lisnih ušiju.
    </p>
    <hr />
    <h2>Upotreba</h2>
    <p>Preporučena količina: 150 metara na 1000 m²</p>
    <p>
      Ljepljive role pričvršćuju se na strukturne elemente (stupove) staklenika i postavljaju se
      paralelno s redovima uzgajanih biljaka. Role treba postaviti malo iznad biljaka. U vrtovima se
      role mogu protezati između stabala. Također se role mogu omotati oko debla stabla kako bi se
      zaštitile od insekata koji se dižu iz zemlje.
    </p>
    <p>
      Koriste se uglavnom za masovno hvatanje štetnika, imaju jak ulovljivi učinak i veliku
      ljepljivu površinu. Ljepljive role FLORA ne podliježu uvjetima okoline, ne gube ljepljivost
      duže vrijeme i otporne su na sunčevu svjetlost (otpornu na UV zračenje).
    </p>
    <p>
      Na stočnim farmama ili u zatvorenim plastenicima ljepljive role nalaze se uz ventilacijske
      dijelove radi kontrole insekata koji se pojavljuju izvana. Na poluotvorenim farmama ili u
      vrtovima ljepljive role protežu se između drveća ili stupova.
    </p>
    <p>
      Žuta boja ljepljive role odražava svjetlost na toj valnoj duljini koja privlači štetočine.
      Insekti lete na rolu, zalijepe se i umiru. Žuta boja naročito dobro privlači bijele mušice.
    </p>
