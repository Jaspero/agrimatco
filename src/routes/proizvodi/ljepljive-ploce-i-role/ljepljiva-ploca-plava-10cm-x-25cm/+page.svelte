    <h1>Ljepljiva ploča za insekte Plava - 10 cm × 25 cm</h1>
    <p>
      FLORA ljepljiva ploča za insekte je ljepljivi mamac sa dvostranim entomološkim ljepljivim
      slojem koji ima snažan učinak hvatanja. Jednostavne su za korištenje, nisu toksične i
      predstavljaju praktično rješenje za suzbijanje štetočina. Mogu se koristiti u plastenicima,
      vrtovima, plantažama i hortikulturi jer privlače i hvataju insekte poput: Tripsa (Frankinella
      occidentalis).
    </p>
    <hr />
    <h2>Upotreba</h2>
    <p>
      Odmah nakon faze sadnje, plave ljepljive ploče treba objesiti na visini od 20 cm iznad visine
      biljaka u količini od 20 komada na 1000 m² (mogu varirati ovisno o situaciji i količini
      štetočina). S rastom biljaka treba povećati visinu ploče. Ukloniti foliju s obje strane ploče.
    </p>
    <hr />
    <h2>Dodatne informacije</h2>
    <p>
      FLORA ljepljive ploče ne podliježu uvjetima okoliša, dugo vremena ne gube ljepljivost, otporne
      su na sunčevu svjetlost (otporne na UV zračenje). Ne lijepe se za ruke i odjeću ljudi tijekom
      upotrebe.
    </p>
    <p>Plava boja privlači insekte poput Thripsa, ali istodobno ne privlači korisne bubice.</p>
    <p>Robu čuvati izvan dosega djece i kućnih ljubimaca.</p>
    <p>Sredstvo ne spada u grupu otrova, te za njega nema karence.</p>
    <p>Zemlja porijekla: Turska</p>
